function inverse(obj) {
  // inverts a dictionary. I'm using this to reverse the gradeweights
  var retobj = {};
  for (var key in obj) {
    retobj[obj[key]] = key;
  }
  return retobj;
}

function compareArrays(array1, array2) {
  if (array1.length !== array2.length) {
      return "Arrays must be of equal length";
  }
  
  let countLarger = 0;
  for (let i = 0; i < array1.length; i++) {
      if (array1[i] >= array2[i]) {
          countLarger++;
      }
  }
  
  const percentLarger = (countLarger / array1.length) * 100;
  return percentLarger;
}

function getAverageVsUserGpa() {
  const table_data = grabTableData();
  const course_names = table_data.map((dict) => dict["course"]);
  const grade_values = table_data.map((dict) => gradeWeights[dict["grade"]]);
  const merged_user_data = Object.fromEntries(
    course_names.map((key, index) => [key, grade_values[index]])
  );

  course_labels = [];
  user_dataset = [];
  avg_student_dataset = [];

  for (const value of course_names) {
    if (course_data[value] != undefined) {
      course_labels.push(value);
      user_dataset.push(merged_user_data[value]);
      avg_student_dataset.push(course_data[value]);
    }
  }

  // this sets the gpa percentage in the info section
  
  abvAvgGpaPercent = document.getElementById("aboveAveragePercentage");
  abvAvgGpaPercent.innerHTML = ~~compareArrays( user_dataset,avg_student_dataset).toFixed() + "%";


  return course_labels, user_dataset, avg_student_dataset;
}




