function calculate() {
  var table_data = grabTableData().filter((dict) => dict.credits > 0);
  var student_gradepoints = 0;
  var student_total_hours = 0;

  for (var i = 0; i < table_data.length; i++) {
    student_gradepoints += table_data[i].gradepoints;
    student_total_hours += Number(table_data[i].credits);
  }

  var student_gpa = (student_gradepoints / student_total_hours) || 0;
  

  document.getElementById("totalGradePoints").innerHTML =
    student_gradepoints.toFixed(2);
  document.getElementById("totalCreditHours").innerHTML =
    student_total_hours.toFixed(2);
  document.getElementById("totalGpa").innerHTML = student_gpa.toFixed(3);

  createChart();
}

function grabTableData() {
  const main = document.getElementById("main");
  var table_data = [];

  for (var i = 1; i < main.childElementCount; i++) {
    var table_row = main.children[i].children;
    var row_semester = table_row[0].children[0].value;
    var row_course = table_row[1].children[0].value;
    var row_credits = table_row[2].children[0].value;
    var row_grade = table_row[3].children[0].value;

    if (row_grade in gradeWeights) {
      var row_gradepoints = row_credits * gradeWeights[row_grade];
    } else {
      var row_gradepoints = 0;
    }
    table_data.push({
      index: i - 1,
      semester: row_semester,
      course: row_course,
      credits: row_credits,
      grade: row_grade,
      gradepoints: row_gradepoints,
    });
  }
  return table_data;
}

